    public void addMessage(Message msg) {
        SwingUtilities.invokeLater(() -> {
            jTextArea1.append(
                    "[" + msg.time + " - "+msg.topic + "] "
                    + msg.clientId
                    + ": "
                    + msg.payload
                    + "\n"
            );
        });
    }
    
    private void requestBuffer() {
        Message msg = new Message();
        msg.type = "GET_BUFFER"; // Solicita o descarregamento do buffer
        msg.clientId = client.getClientId();
        client.send(msg);
    }
    private void requestTopics() {

        Message msg = new Message();

        msg.type = "LIST_MY_TOPICS"; // ou LIST_ALL_TOPICS
        msg.clientId = client.getClientId();

        client.send(msg);
    }
    public void updateTopics(List<String> topics) {

        SwingUtilities.invokeLater(() -> {

            topic.removeAllItems();

            for (String t : topics) {
                topic.addItem(t);
            }
        });
    }
