    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        String selectedTopic = topic.getSelectedItem().toString();

        String payload = message.getText().trim();

        if(payload.isEmpty()){
            return;
        }

        Message msg = new Message();

        msg.type = "PUBLISH";
        msg.clientId = client.getClientId();
        msg.topic = selectedTopic;
        msg.payload = payload;
        msg.date = LocalDate.now().toString();
        msg.time = LocalTime.now().format(DateTimeFormatter.BASIC_ISO_DATE.ofPattern("HH:mm:ss"));

        client.send(msg);
        message.setText("");
        
    }//GEN-LAST:event_sendActionPerformed

