    private void unsubscribeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_unsubscribeBtnActionPerformed
        String selectedTopic = (topic.getSelectedItem() != null) ? topic.getSelectedItem().toString() : "";
        
        if (selectedTopic.isEmpty() || selectedTopic.equals("Carregando seus tópicos...") || selectedTopic.equals("Nenhum tópico inscrito")) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione um tópico válido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Monta a mensagem de UNSUBSCRIBE igualzinho ao Tx.java original
        Message msg = new Message();
        msg.type = "UNSUBSCRIBE";
        msg.clientId = client.getClientId();
        msg.topic = selectedTopic;
        msg.payload = null;
        msg.date = java.time.LocalDate.now().toString();
        msg.time = java.time.LocalTime.now().toString();

        client.send(msg);

        JOptionPane.showMessageDialog(this, "Você saiu do tópico: " + selectedTopic, "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        
        client.setUnsubscribeView(null); // Limpa o vínculo
        dispose(); // Fecha a janela após a requisição
    }//GEN-LAST:event_unsubscribeBtnActionPerformed

