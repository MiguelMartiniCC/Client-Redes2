    public void updateTopics(List<String> topicsList) {
        SwingUtilities.invokeLater(() -> {
            topic.removeAllItems();
            
            if (topicsList == null || topicsList.isEmpty()) {
                topic.addItem("Nenhum tópico inscrito");
                unsubscribeBtn.setEnabled(false); // Desativa o botão se não houver tópicos
                return;
            }
            
            unsubscribeBtn.setEnabled(true);
            for (String t : topicsList) {
                topic.addItem(t);
            }
        });
    }

